namespace Ficsit.Schematics.Core.GameData;

/// <summary>
/// A machine/belt mark (Mk.1–Mk.6). The numeric value is the mark number, so the
/// catalog can render the game name strings ("Miner Mk.1", "Mk.3 Belt") from it.
/// </summary>
public enum Mark
{
    Mk1 = 1,
    Mk2 = 2,
    Mk3 = 3,
    Mk4 = 4,
    Mk5 = 5,
    Mk6 = 6,
}
